package org.lynxsdk.library

class LynxLibraryRegistryGenerator {
    static final String REGISTRY_PACKAGE_NAME = 'com.lynx.tasm.library'
    static final String REGISTRY_CLASS_NAME = 'LynxAutolinkGenerated'

    static String generate(List<LynxLibraryInfo> libraries) {
        return generateRegistry(libraries)
    }

    static String generateRegistry(List<LynxLibraryInfo> libraries) {
        String providerEntries = libraries.collect {
            "      \"${it.providerClassName}\""
        }.join(',\n')
        if (providerEntries.length() == 0) {
            providerEntries = ''
        }
        """// Generated by org.lynxsdk.library. Do not edit.
package ${REGISTRY_PACKAGE_NAME};

import android.content.Context;
import androidx.annotation.Keep;
import com.lynx.tasm.LynxViewBuilder;

@Keep
public final class ${REGISTRY_CLASS_NAME} {
  private static final String[] PROVIDERS = new String[] {
${providerEntries}
  };

  private ${REGISTRY_CLASS_NAME}() {}

  public static void setupGlobal(Context context) {
    LynxLibraryRegistry.setupGlobal(context, PROVIDERS);
  }

  public static void setup(LynxViewBuilder builder) {
    LynxLibraryRegistry.setup(builder, PROVIDERS);
  }
}
"""
    }
}
